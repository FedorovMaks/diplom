function specsFor(subjectsBlob) {
  const subjects = subjectsBlob.split(';').map((s) => s.trim());
  const tags = new Set();
  for (const subj of subjects) {
    for (const [re, marks] of KEYWORDS) {
      if (re.test(subj)) marks.forEach((m) => tags.add(m));
    }
  }
  return tags;
}

function groupsFor(tags) {
  if (tags.has('ALL')) return [...GROUPS];
  const prefixMap = { ISP: 'ИСП-', Y: 'Ю-', D: 'Д-', R: 'Р-', E: 'Э-' };
  const out = [];
  for (const tag of tags) {
    const pref = prefixMap[tag];
    if (!pref) continue;
    GROUPS.filter((g) => g.startsWith(pref)).forEach((g) => out.push(g));
  }
  return [...new Set(out)];
}

const teachers = SOURCE.map(([fio, subjects], i) => {
  const tags = specsFor(subjects);
  return {
    id: i + 1,
    fio,
    subjects: subjects.split(';').map((s) => s.trim()),
    groups: groupsFor(tags),
  };
});

const target = path.join(__dirname, '..', 'data', 'teachers.json');
fs.writeFileSync(target, JSON.stringify(teachers, null, 2), 'utf8');
console.log(`Written ${teachers.length} teachers → ${target}`);

const noGroups = teachers.filter((t) => t.groups.length === 0);
if (noGroups.length) {
  console.warn(`\nNo group match for ${noGroups.length} teachers (отредактируйте teachers.json вручную):`);
  noGroups.forEach((t) => console.warn(`  - ${t.fio}: ${t.subjects.join(' | ')}`));
}
